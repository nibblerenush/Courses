#include <iostream>
#include <map>
#include <string>
#include <tuple>
#include <vector>

using namespace std;

enum class Lang {
  DE, FR, IT
};

struct Region {
  string std_name;
  string parent_std_name;
  map<Lang, string> names;
  int64_t population;
};

int FindMaxRepetitionCount(const vector<Region>& regions)
{
  if (regions.empty())
  {
    return 0;
  }
  
  map<tuple<const string&, const string&, const map<Lang, string>&, const int64_t&>, int> region_count;
  for (const auto& region : regions)
  {
    ++region_count[tie(region.std_name, region.parent_std_name, region.names, region.population)];
  }

  int result{0};
  for (const auto& item : region_count)
  {
    if (item.second > result)
    {
      result = item.second;
    }
  }

  return result;
}

